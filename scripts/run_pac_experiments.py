#!/usr/bin/env python3
"""Generate and run Provisional ASR Context decoding experiment configs."""

from __future__ import annotations

import argparse
import os
import subprocess
from pathlib import Path
from typing import Any

try:
    import yaml
except ImportError as exc:  # pragma: no cover - dependency message
    raise SystemExit("PyYAML is required: pip install pyyaml") from exc


def load_yaml(path: Path) -> dict[str, Any]:
    with path.open(encoding="utf-8") as f:
        data = yaml.safe_load(f)
    if not isinstance(data, dict):
        raise SystemExit(f"Expected YAML mapping: {path}")
    return data


def write_yaml(path: Path, data: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        f.write("# Auto-generated by scripts/run_pac_experiments.py.\n")
        f.write("# Edit pac_experiment_manifest.yaml, not this file.\n\n")
        yaml.safe_dump(data, f, sort_keys=False)


def repo_root_from_manifest(manifest_path: Path) -> Path:
    return manifest_path.resolve().parent


def resolve_repo_path(repo_root: Path, value: str | Path) -> Path:
    path = Path(value)
    if path.is_absolute():
        return path
    return repo_root / path


def experiment_output_dir(
    repo_root: Path,
    manifest: dict[str, Any],
    experiment: dict[str, Any],
    acl_set: str = "eval",
) -> Path:
    output_dir = experiment.get("output_dir")
    if output_dir:
        base = resolve_repo_path(repo_root, output_dir)
    else:
        output_root = resolve_repo_path(repo_root, manifest.get("output_root", "output/pac_experiments"))
        base = output_root / experiment["id"]
    if acl_set != "eval":
        base = base / acl_set
    return base


def resolve_acl_set(cli_value: str | None, manifest: dict[str, Any]) -> str:
    acl_set = cli_value or manifest.get("acl_set", manifest.get("set", "eval"))
    if acl_set not in {"eval", "dev"}:
        raise SystemExit(f"ACL 60-60 set must be 'eval' or 'dev' (got: {acl_set!r})")
    return acl_set


def generated_config_path(repo_root: Path, manifest: dict[str, Any], experiment: dict[str, Any]) -> Path:
    output_root = resolve_repo_path(repo_root, manifest.get("output_root", "output/pac_experiments"))
    return output_root / "configs" / f"{experiment['id']}.yaml"


def experiment_base_config(repo_root: Path, manifest: dict[str, Any], experiment: dict[str, Any]) -> Path:
    base_config = experiment.get("base_config", manifest.get("base_config"))
    if not base_config:
        raise SystemExit(f"Missing base_config for experiment {experiment['id']}")
    return resolve_repo_path(repo_root, base_config)


def generate_config(repo_root: Path, manifest: dict[str, Any], experiment: dict[str, Any]) -> Path | None:
    if not experiment.get("enabled", True):
        return None
    base_config_path = experiment_base_config(repo_root, manifest, experiment)
    config = load_yaml(base_config_path)
    config.update(experiment.get("overrides", {}))
    config["experiment_id"] = experiment["id"]
    config["experiment_label"] = experiment.get("label", experiment["id"])
    config_path = generated_config_path(repo_root, manifest, experiment)
    write_yaml(config_path, config)
    return config_path


def selected_experiments(manifest: dict[str, Any], ids: set[str] | None) -> list[dict[str, Any]]:
    experiments = manifest.get("experiments", [])
    if ids is None:
        return experiments
    known = {experiment["id"] for experiment in experiments}
    missing = ids - known
    if missing:
        raise SystemExit(f"Unknown experiment id(s): {', '.join(sorted(missing))}")
    return [experiment for experiment in experiments if experiment["id"] in ids]


def run_command(cmd: list[str], env: dict[str, str], dry_run: bool) -> None:
    printable = " ".join(cmd)
    print(f"\n$ {printable}")
    if dry_run:
        return
    subprocess.run(cmd, env=env, check=True)


def run_experiment(
    repo_root: Path,
    manifest: dict[str, Any],
    experiment: dict[str, Any],
    config_path: Path,
    directions: list[str],
    acl_set: str,
    args: argparse.Namespace,
) -> None:
    out_dir = experiment_output_dir(repo_root, manifest, experiment, acl_set)
    env = os.environ.copy()
    env["SPEECH_CFG"] = str(config_path)
    env["OUTPUT_DIR"] = str(out_dir)
    env["ACL6060_SET"] = acl_set

    if args.score_only:
        cmd = [str(repo_root / "score_acl6060_metrics.sh"), acl_set, *directions]
    else:
        cmd = [str(repo_root / "run_acl6060_simulstream.sh"), acl_set, *directions]
    run_command(cmd, env, args.dry_run)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--manifest", default="pac_experiment_manifest.yaml", type=Path)
    parser.add_argument("--experiments", nargs="*", help="Optional experiment IDs to run.")
    parser.add_argument("--directions", nargs="*", help="Override manifest directions.")
    parser.add_argument(
        "--set",
        dest="acl_set",
        choices=("eval", "dev"),
        help="ACL 60-60 split to run (default: manifest acl_set/set, else eval).",
    )
    parser.add_argument("--generate-only", action="store_true", help="Only write generated YAML configs.")
    parser.add_argument("--score-only", action="store_true", help="Score existing metrics logs without inference.")
    parser.add_argument("--report-only", action="store_true", help="Only regenerate the aggregate PAC report.")
    parser.add_argument("--skip-report", action="store_true", help="Do not run scripts/pac_experiment_report.py.")
    parser.add_argument("--dry-run", action="store_true", help="Print commands without running them.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    manifest_path = args.manifest.resolve()
    repo_root = repo_root_from_manifest(manifest_path)
    manifest = load_yaml(manifest_path)
    acl_set = resolve_acl_set(args.acl_set, manifest)
    selected_ids = set(args.experiments) if args.experiments else None
    experiments = selected_experiments(manifest, selected_ids)
    directions = args.directions or manifest.get(
        "directions", ["en-de", "en-fr", "en-nl", "en-pt", "en-ru", "en-tr"]
    )

    print(f"Using ACL 60-60 set: {acl_set}")

    generated: dict[str, Path] = {}
    for experiment in experiments:
        config_path = generate_config(repo_root, manifest, experiment)
        if config_path is None:
            print(f"Skipping disabled experiment: {experiment['id']}")
            continue
        generated[experiment["id"]] = config_path
        print(f"Generated {config_path}")

    if not args.generate_only and not args.report_only:
        for experiment in experiments:
            config_path = generated.get(experiment["id"])
            if config_path is None:
                continue
            run_experiment(repo_root, manifest, experiment, config_path, directions, acl_set, args)

    if not args.skip_report and not args.generate_only:
        report_cmd = [
            os.environ.get("PYTHON", "python3"),
            str(repo_root / "scripts" / "pac_experiment_report.py"),
            "--manifest",
            str(manifest_path),
            "--set",
            acl_set,
        ]
        if selected_ids:
            report_cmd.extend(["--experiments", *sorted(selected_ids)])
        run_command(report_cmd, os.environ.copy(), args.dry_run)


if __name__ == "__main__":
    main()
